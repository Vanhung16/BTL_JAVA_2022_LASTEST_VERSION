    private void btnAddItemActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_btnAddItemActionPerformed
        // TODO add your handling code here:
        String ten_mon = (String) cbMonHoc.getSelectedItem();
        String min_sv = txtMinHS.getText();
        String max_sv = txtMaxHS.getText();
        String time_start = txtTimeStart.getText();
        String so_phong = txtSoPhong.getText();
        String ten_giao_vien = (String) cbGiaoVien.getSelectedItem();
        String so_tuan_hoc = txtSoTuan.getText();

        if (ten_mon.equals("") || min_sv.equals("") || max_sv.equals("") || time_start.equals("")
                || so_phong.equals("") || ten_giao_vien.equals("") || so_tuan_hoc.equals("")) {
            JOptionPane.showMessageDialog(this, "Yêu cầu nhập đủ các trường !",
                    "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!CommonUtilis.isNumeric(min_sv) || !CommonUtilis.isNumeric(max_sv) || !CommonUtilis.isNumeric(so_tuan_hoc)) {
            JOptionPane.showMessageDialog(this, "Yêu cầu số lượng HS tối thiểu, tối đa, số tuần học"
                    + " phải là số !",
                    "Thông báo", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int id_mon_hoc = 0;
        for (MonHoc monHoc : dsMonHoc) {
            if (ten_mon.equals(monHoc.getName())) {
                id_mon_hoc = monHoc.getId();
                break;
            }
        }
        int id_giao_vien = 0;
        for (GiaoVien giaoVien : dsGiaoVien) {
            if (ten_giao_vien.equals(giaoVien.getName())) {
                id_giao_vien = giaoVien.getId();
                break;
            }
        }
        try {
            String sql = "INSERT INTO `tkb_mh`"
                    + "(`id_mon_hoc`, `min_sv`, `max_sv`, `time_start`, `so_phong`, `id_giao_vien`, `so_tuan_hoc`) "
                    + "VALUES ('" + id_mon_hoc + "','" + min_sv + "','"
                    + max_sv + "','" + time_start + "','" + so_phong + "','"
                    + id_giao_vien + "','" + so_tuan_hoc + "')";
            services.Services.post(sql);
            fetchData();
            txtMinHS.setText("");
            txtMaxHS.setText("");
            txtTimeStart.setText("");
            txtSoPhong.setText("");
            txtSoTuan.setText("");
        } catch (Exception e) {
           JOptionPane.showMessageDialog(this, "Môn học đã được tạo, hãy chọn môn khác",
                    "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }// GEN-LAST:event_btnAddItemActionPerformed

    public void fetchData() {
        // TODO add your handling code here:

        dsTKBMH = new ArrayList<>();
        try {
            String sql = "SELECT tkb_mh.id,min_sv,max_sv,time_start,so_phong,so_tuan_hoc,"
                    + " mon_hoc.name as \"ten_mon_hoc\", giao_vien.name as \"ten_giao_vien\" "
                    + "FROM `tkb_mh` INNER JOIN mon_hoc on tkb_mh.id_mon_hoc = mon_hoc.id "
                    + "INNER JOIN giao_vien ON tkb_mh.id_giao_vien = giao_vien.id";
            ResultSet rs = services.Services.get(sql);
            TKBMH x = null;
            while (rs.next()) {
                x = new TKBMH(rs.getInt("tkb_mh.id"), rs.getString("ten_mon_hoc"),
                        rs.getInt("min_sv"), rs.getInt("max_sv"), rs.getString("time_start"),
                        rs.getString("so_phong"), rs.getString("ten_giao_vien"),
                        rs.getInt("so_tuan_hoc"));
                dsTKBMH.add(x);
            }
            LoadTableSchedule();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, "Dữ liệu không hợp lệ",
                    "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
    }

