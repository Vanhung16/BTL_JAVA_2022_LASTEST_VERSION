    public void fetchDataCB() {
        try {
            String sql = "SELECT  mon_hoc.id,mon_hoc.name,mon_hoc.so_tin_chi,nhom_mon.name "
                    + "as ten_nhom FROM `mon_hoc` INNER JOIN nhom_mon ON mon_hoc.id_nhom = nhom_mon.id\n";
            ResultSet rs = services.Services.get(sql);
            MonHoc x = null;
            while (rs.next()) {
                x = new MonHoc(rs.getInt("mon_hoc.id"), rs.getString("mon_hoc.name"), rs.getInt("mon_hoc.so_tin_chi"),
                        rs.getString("ten_nhom"));
                dsMonHoc.add(x);
            }
        } catch (Exception e) {
            System.out.println(e);
        }

        for (MonHoc monHoc : dsMonHoc) {
            cbMonHoc.addItem(monHoc.getName());
        }

        try {
            String sql = "SELECT `id`, `name`, `address`, `phone` FROM `giao_vien` WHERE 1";
            ResultSet rs = Services.get(sql);
            GiaoVien x = null;
            while (rs.next()) {
                x = new GiaoVien(rs.getInt("id"), rs.getString("name"), rs.getString("address"), rs.getString("phone"));
                dsGiaoVien.add(x);
            }
        } catch (SQLException ex) {
            System.out.println(ex);
        }

        for (GiaoVien giaoVien : dsGiaoVien) {
            cbGiaoVien.addItem(giaoVien.getName());
        }
    }

